SELECT
    CAST(SUBSTRING(DATETIME, 12, 2) AS DECIMAL) AS HOUR,
    COUNT(*) AS COUNT
FROM
    ANIMAL_OUTS
GROUP BY
    HOUR
HAVING
    HOUR >= 9
   AND
    HOUR <= 19
ORDER BY
    HOUR


